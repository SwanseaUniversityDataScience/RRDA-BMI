INSERT INTO SAILWXXXXV.HDR25_BMI_CLEAN_CYP_STAGE_1
SELECT
		alf_e,
		sex,
		wob,
		age_months,
		age_years,
		age_band,
		bmi_dt,
		bmi_val,
