			WHEN BMI_val >= 25.0  AND BMI_val < 30 	THEN 'Overweight'
			WHEN BMI_val >= 30.0 					THEN 'Obese'
			ELSE NULL 
			END AS BMI_val_cat, -- all the appropriate values will be assigned these categories. 
		BMI_percentile, 
		percentile_BMI_cat,
		BMI_z_score, 
		z_score_BMI_cat, 
		height, 
		weight, 
		source_type, 
		source_rank, 
		source_db,
		active_from,
		active_to,
		dod,
		follow_up_dod,
		follow_up_res
	FROM
		SAILWXXXXV.HDR25_BMI_COMBO_CYP_STAGE_3 -- table with BMI categories assigned to BMI values for CYP.
	WHERE 
	 	abs((DAYS_BETWEEN(bmi_dt, wob)/365.25 >= 2 -- 2 years old
	 	AND DAYS_BETWEEN(bmi_dt, wob)/365.25 < 19)) -- before they turn 19
	)
;

COMMIT;

